// YouTube transcript retrieval — ported from the proven recipe in the
// youtube-transcript package (as used by Gumshoe): ask the InnerTube player
// endpoint as the ANDROID client, whose caption URLs are not gated behind
// proof-of-origin tokens, then parse the caption XML.
// Adapted for a content-script context: no custom User-Agent (forbidden
// header) and cookies omitted so the request looks anonymous, matching the
// server-side recipe as closely as the browser allows.
globalThis.TDDW = globalThis.TDDW || { adapters: [] };

(() => {
  const INNERTUBE_URL = 'https://www.youtube.com/youtubei/v1/player?prettyPrint=false';
  const INNERTUBE_CONTEXT = {
    client: { clientName: 'ANDROID', clientVersion: '20.10.38' }
  };

  function decodeEntities(text) {
    return text
      .replace(/&lt;/g, '<')
      .replace(/&gt;/g, '>')
      .replace(/&quot;/g, '"')
      .replace(/&#39;/g, "'")
      .replace(/&apos;/g, "'")
      .replace(/&#x([0-9a-fA-F]+);/g, (_, hex) => String.fromCodePoint(parseInt(hex, 16)))
      .replace(/&#(\d+);/g, (_, dec) => String.fromCodePoint(parseInt(dec, 10)))
      // &amp; must be decoded LAST: doing it first would let a literal
      // "&amp;lt;" collapse to "<" instead of the intended "&lt;".
      .replace(/&amp;/g, '&');
  }

  // Prefer a human-made track in the UI language, then English, then
  // anything; auto-generated (kind: "asr") ranks below human captions.
  function pickCaptionTrack(tracks) {
    const lang = (navigator.language || 'en').slice(0, 2);
    const score = (t) => {
      let s = 0;
      if (t.languageCode?.startsWith(lang)) s += 4;
      else if (t.languageCode?.startsWith('en')) s += 2;
      if (t.kind !== 'asr') s += 1;
      return s;
    };
    return [...tracks].sort((a, b) => score(b) - score(a))[0] || null;
  }

  function formatTimestamp(ms) {
    const s = Math.floor(ms / 1000);
    const h = Math.floor(s / 3600);
    const m = Math.floor((s % 3600) / 60);
    const sec = String(s % 60).padStart(2, '0');
    return h > 0 ? `${h}:${String(m).padStart(2, '0')}:${sec}` : `${m}:${sec}`;
  }

  // Caption XML comes in two formats:
  //   srv3:    <p t="ms" d="ms"><s>wo</s><s>rd</s></p>
  //   classic: <text start="s" dur="s">escaped text</text>
  // Each output line is prefixed with its [m:ss] timestamp so the recap
  // bullets can carry seekable time chips.
  function parseCaptionXml(xml, cutoffMs) {
    const lines = [];

    const pRegex = /<p\s+t="(\d+)"[^>]*>([\s\S]*?)<\/p>/g;
    let m;
    while ((m = pRegex.exec(xml)) !== null) {
      const startMs = parseInt(m[1], 10);
      if (startMs > cutoffMs) continue;
      let text = '';
      const sRegex = /<s[^>]*>([^<]*)<\/s>/g;
      let s;
      while ((s = sRegex.exec(m[2])) !== null) text += s[1];
      if (!text) text = m[2].replace(/<[^>]+>/g, '');
      text = decodeEntities(text).replace(/\s+/g, ' ').trim();
      if (text) lines.push(`[${formatTimestamp(startMs)}] ${text}`);
    }
    if (lines.length) return lines;

    for (const t of xml.matchAll(/<text start="([^"]*)"[^>]*>([^<]*)<\/text>/g)) {
      const startMs = parseFloat(t[1]) * 1000;
      if (startMs > cutoffMs) continue;
      const text = decodeEntities(t[2]).replace(/\s+/g, ' ').trim();
      if (text) lines.push(`[${formatTimestamp(startMs)}] ${text}`);
    }
    return lines;
  }

  async function fetchCaptionTracks(videoId) {
    const res = await fetch(INNERTUBE_URL, {
      method: 'POST',
      credentials: 'omit',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ context: INNERTUBE_CONTEXT, videoId })
    });
    if (!res.ok) throw new Error(`innertube player failed (${res.status})`);
    const data = await res.json();
    return data?.captions?.playerCaptionsTracklistRenderer?.captionTracks || null;
  }

  // Returns { lines, language, autoGenerated } | null (no captions).
  globalThis.TDDW.fetchYouTubeTranscript = async function (videoId, cutoffMs) {
    const tracks = await fetchCaptionTracks(videoId);
    if (!tracks?.length) return null;

    const track = pickCaptionTrack(tracks);
    const capRes = await fetch(track.baseUrl, { credentials: 'omit' });
    if (!capRes.ok) throw new Error(`caption fetch failed (${capRes.status})`);
    const xml = await capRes.text();
    if (!xml) throw new Error('caption endpoint returned an empty body');

    const lines = parseCaptionXml(xml, cutoffMs);
    return lines.length
      ? { lines, language: track.languageCode || 'unknown', autoGenerated: track.kind === 'asr' }
      : null;
  };

  globalThis.TDDW.pickCaptionTrack = pickCaptionTrack;
  // Shared so the page-captions fallback in youtube.js can prefix its lines with
  // the same [m:ss] timestamps the InnerTube path produces.
  globalThis.TDDW.formatTimestamp = formatTimestamp;
})();
